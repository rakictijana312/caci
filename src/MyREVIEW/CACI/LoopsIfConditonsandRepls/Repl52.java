package MyREVIEW.CACI.LoopsIfConditonsandRepls;

public class Repl52 {
    /*
Create a do while loop that prints out "Hello World!" 5 times.
Output:
Hello World!
Hello World!
Hello World!
Hello World!
Hello World!
     */

    public static void main(String[] args) {
        int i=0;
        do{
            System.out.println("Hello world");
            i++;
        }while (i<5)  ;
    }

}
